package com.yantianpeng.day_03;

/**
 * 赋值运算符，赋值扩展符号
 */
public class TestOperator2 {

    public static void main(String[] args) {
        int a  =3;
        int b =4;
        a+=b;//相当于 a = a+b;
        System.out.println(a);

        a-=b;//相当于a = a-b;
        System.out.println(a);

        a*=b;//相当于 a = a*b;
        System.out.println(a);

        a/=b;//相当于a = a/b;

        System.out.println(a);





    }
}
